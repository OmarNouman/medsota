<script>
    function swalDelete(form, title, text) {
        return function fire() {
            form.submit(function(e) {
                e.preventDefault();
            });
            Swal.fire({
                title: title,
                text: text,
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Yes, delete it!'
            }).then((result) => {
                if (result.isConfirmed) {
                    form.submit();
                };
            });
        }
    };
</script>

